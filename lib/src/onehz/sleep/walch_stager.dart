/// Auto-calibrate 1 g reference: median magnitude over the low-motion portion.
double _calibrateG(List<double> mag) {
  if (mag.isEmpty) return 1.0;
  // |Δ‖a‖| per sample; still = below the median step.
  final steps = <double>[];
  for (var i = 1; i < mag.length; i++) {
    steps.add((mag[i] - mag[i - 1]).abs());
  }
  final stepMed = steps.isNotEmpty ? (median(steps) ?? 0) : 0;
  final still = <double>[];
  for (var i = 1; i < mag.length; i++) {
    if ((mag[i] - mag[i - 1]).abs() <= stepMed) still.add(mag[i]);
  }
  final g = still.isNotEmpty ? median(still) : median(mag);
  if (g == null || g <= 0 || g.isNaN) return 1.0;
  return g;
}

/// Carry-forward interpolation of HR over off-skin (0) gaps; leading 0s filled
/// with the first valid value, so the series is dense like Walch's np.interp.
List<double> _interpolateHr(List<double> hr, int n) {
  final out = List<double>.filled(n, 0);
  // First valid.
  var firstValid = double.nan;
  for (var i = 0; i < n; i++) {
    if (hr[i] > 0) {
      firstValid = hr[i];
      break;
    }
  }
  if (firstValid.isNaN) return out; // all off-skin
  var last = firstValid;
  for (var i = 0; i < n; i++) {
    if (hr[i] > 0) last = hr[i];
    out[i] = last;
  }
  return out;
}

/// Difference-of-Gaussians convolution, VERBATIM port of utils.convolve_with_dog
/// (σ1=120, σ2=600, scalar=0.75, mean-subtracted, boundary-reflected padding).
/// [boxPts] = WINDOW_SIZE. Returns a series the same length as [y].
List<double> _convolveWithDog(List<double> y, int boxPts) {
  final m = mean(y) ?? 0;
  final yc = [for (final v in y) v - m];

  final box = List<double>.filled(boxPts, 0);
  final mu1 = (boxPts / 2.0).toInt();
  const sigma1 = 120.0;
  final mu2 = (boxPts / 2.0).toInt();
  const sigma2 = 600.0;
  const scalar = 0.75;
  for (var i = 0; i < boxPts; i++) {
    box[i] = math.exp(-0.5 * math.pow((i - mu1) / sigma1, 2)) -
        scalar * math.exp(-0.5 * math.pow((i - mu2) / sigma2, 2));
  }

  // Pad by reflecting boundary: prepend flip(y[0:boxPts/2]), append
  // flip(y[-boxPts/2:]) — matching numpy insert in utils.convolve_with_dog.
  final half = (boxPts / 2).toInt();
  final padded = <double>[];
  for (var i = math.min(half, yc.length) - 1; i >= 0; i--) {
    padded.add(yc[i]); // flip of first `half`
  }
  padded.addAll(yc);
  final tail = <double>[];
  for (var i = yc.length - 1; i >= math.max(0, yc.length - half); i--) {
    tail.add(yc[i]); // flip of last `half`
  }
  padded.addAll(tail);

  // 'valid' convolution: output length = len(padded) - boxPts + 1.
  final outLen = padded.length - boxPts + 1;
  if (outLen <= 0) {
    // Window longer than signal: fall back to a single global value broadcast.
    return List<double>.filled(y.length, 0);
  }
  final conv = List<double>.filled(outLen, 0);
  for (var k = 0; k < outLen; k++) {
    var acc = 0.0;
    for (var j = 0; j < boxPts; j++) {
      acc += padded[k + j] * box[boxPts - 1 - j];
    }
    conv[k] = acc;
  }
  // np.convolve(..., 'valid') on a (len+2*half)-padded signal returns
  // ~len(y)+1 samples; we clip/pad to exactly len(y).
  if (conv.length >= y.length) {
    return conv.sublist(0, y.length);
  }
  final out = List<double>.from(conv);
  while (out.length < y.length) {
    out.add(conv.isNotEmpty ? conv.last : 0.0);
  }
  return out;
}

/// Gaussian-weighted single value over [lo,hi), VERBATIM port of
/// utils.smooth_gauss: kernel length = window length, μ = len/2, σ = 50 s.
double _smoothGauss(List<double> y, int lo, int hi) {
  final len = hi - lo;
  if (len <= 0) return 0;
  final mu = (len / 2.0).toInt();
  const sigma = 50.0;
  var wsum = 0.0;
  final w = List<double>.filled(len, 0);
  for (var i = 0; i < len; i++) {
    w[i] = math.exp(-0.5 * math.pow((i - mu) / sigma, 2));
    wsum += w[i];
  }
  if (wsum == 0) return 0;
  var acc = 0.0;
  for (var i = 0; i < len; i++) {
    acc += (w[i] / wsum) * y[lo + i];
  }
  return acc;
}

/// Population/sample std over [lo,hi) (Walch uses numpy np.std = population).
double _stdRange(List<double> y, int lo, int hi) {
  final len = hi - lo;
  if (len <= 0) return 0;
  var sum = 0.0;
  for (var i = lo; i < hi; i++) {
    sum += y[i];
  }
  final m = sum / len;
  var v = 0.0;
  for (var i = lo; i < hi; i++) {
    final d = y[i] - m;
    v += d * d;
  }
  return math.sqrt(v / len); // np.std default = population (ddof=0)
}

/// Cosine clock proxy, VERBATIM port of TimeBasedFeatureService.cosine_proxy:
///   -cos((t - 5h)·2π / 24h),  t in seconds.
double _cosineProxy(double tSec) {
  const shiftSec = 5 * 3600.0; // sleep_drive_cosine_shift = 5 hours
  const day = 24 * 3600.0;
  return -1 * math.cos((tSec - shiftSec) * 2 * math.pi / day);
}

// ---------------------------------------------------------------------------
// Post-processing: Webster rescore (private copy — keeps walch_stager self-
// contained; identical rules to stager.dart). consolidateSleepStages is reused.
// ---------------------------------------------------------------------------

void _websterRescore(List<SleepStage> sm, int epochSec) {
  bool isSleep(SleepStage s) => s != SleepStage.wake;
  final n = sm.length;
  double minToEp(double m) => m * 60.0 / epochSec;

  var onset = -1, lastSleep = -1;
  for (var i = 0; i < n; i++) {
    if (isSleep(sm[i])) {
      if (onset < 0) onset = i;
      lastSleep = i;
    }
  }
  if (onset < 0) return;

  int sleepRunBefore(int i) {
    var c = 0, k = i - 1;
    while (k >= onset && isSleep(sm[k])) {
      c++;
      k--;
    }
    return c;
  }

  int sleepRunAfter(int i) {
    var c = 0, k = i + 1;
    while (k <= lastSleep && isSleep(sm[k])) {
      c++;
      k++;
    }
    return c;
  }

  final rules = <List<double>>[
    [minToEp(15), minToEp(5)],
    [minToEp(10), minToEp(3)],
    [minToEp(4), minToEp(1)],
  ];

  var i = onset;
  while (i <= lastSleep) {
    if (isSleep(sm[i])) {
      i++;
      continue;
    }
    var j = i;
    while (j <= lastSleep && !isSleep(sm[j])) {
      j++;
    }
    final wakeLen = j - i;
    final before = sleepRunBefore(i);
    final after = sleepRunAfter(j - 1);
    final context = math.max(before, after).toDouble();
    var rescore = false;
    for (final r in rules) {
      if (context >= r[0] && wakeLen <= r[1]) {
        rescore = true;
        break;
      }
    }
    if (rescore) {
      for (var k = i; k < j; k++) {
        sm[k] = SleepStage.nrem;
      }
    }
    i = j;
  }
}

/// Merge Deep bouts shorter than 3 min into Light (false), so Deep reads as
/// consolidated SWS rather than single-epoch flicker. LOW-CONFIDENCE overlay.
void _mergeShortDeepBouts(
    List<bool> deepFlag, List<SleepStage> sm, int epochSec) {
  final minEp = (3 * 60.0 / epochSec).round();
  if (minEp <= 1) return;
  final n = deepFlag.length;
  var i = 0;
  while (i < n) {
    if (!deepFlag[i] || sm[i] != SleepStage.nrem) {
      i++;
      continue;
    }
    var j = i;
    while (j < n && deepFlag[j] && sm[j] == SleepStage.nrem) {
      j++;
    }
    if ((j - i) < minEp) {
      for (var k = i; k < j; k++) {
        deepFlag[k] = false;
      }
    }
    i = j;
  }
}
